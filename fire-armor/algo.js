function moveZeros(arr) {
    for(let i = arr.length-1, j = i; i >= 0; i--) {
        for(; arr[j] != 0; j--); 
        if(arr[i] == 0) {
            let t = arr[i];
            arr[i] = arr[j];
            arr[j] = t;
        }
    }
    return arr
}



